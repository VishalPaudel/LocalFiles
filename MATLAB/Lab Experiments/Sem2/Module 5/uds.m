    x2(i) = x2(i) - months;

    if (x2(i) >= 0 && x2(i) < 54)
        y = (5*10^(-6)*(x2(i)^3)) + (7*10^(-5)*(x2(i)^2)) + (0.002*x2(i)) + 0.0011;
    else
        y = 1;
    end
        u(i) = k1*u1 + k2*y;
