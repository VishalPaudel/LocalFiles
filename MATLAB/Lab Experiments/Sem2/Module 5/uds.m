k2 = 0.5;
x1 = 1500;
months = 24;

if (x1 >= 0 && x1 < 1000)
    u1 = 1;
elseif (x1 >= 1000 && x1 < 2950)
    u1 = ((-6 * 10^(-11))*x1^3) + ((1.3 * 10^(-7))*x1^2) - ((2.64 * 10^(-4))*x1) + 1.1907;
else
    u1 = 0;
end

